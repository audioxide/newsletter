                                    <p>A big milestone has been reached on the site development front, and you’re looking at it. After tinkering away off and on for months we’ve finally built our own custom newsletter generator. We put in the date range and a script generates 90% of a newsletter for us using the site API.</p>
                                    <p>This is part of a larger project to bring our monthly newsletters in-house as a kind of archive. Although it’s not quite there yet, we’ve got the shell of one going using <a href="https://kit.svelte.dev/" style="color: #f01d4f;">SvelteKit</a>. A work in progress, but not looking bad.</p>
