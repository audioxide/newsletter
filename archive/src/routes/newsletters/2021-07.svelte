                        <h2>This really is It</h2>
                        <p>A stop-start month here as our lives slowly came out of hibernation. There are worse problems to have.</p>
                        <p>And there are worse artist around than Tyler, the Creator. In fact, it feels like there are few better at the moment. His new album <strong><em>CALL ME IF YOU GET LOST</em></strong> was an eclectic delight. The man can do no wrong.</p>
                        <p>Elsewhere we returned to our insufferable teen days to celebrate <strong><em>Is This It</em></strong> by The Strokes turning 20. We’re glad to report it’s as cool as ever. And finally we took inspiration from Kanye West’s creative process on <strong><em>Donda</em></strong>.</p>
                        <p>We continue to chip away at album artwork credits, and for those of you who think things look a little different this month, that’s because we’ve hit a major milestone on a new site feature.</p>
                        <p>Be well,</p>
                        <p>André, Fred, and Andrew </p>
